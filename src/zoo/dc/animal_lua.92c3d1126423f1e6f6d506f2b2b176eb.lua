--local function checkColRange(range,colValue,type)
local function checkColRange(range,colValue,type1)
    flag = false
    numstart = 0
    numend = 0
    arrTab = LuaDpSDK:split(range, "-")
    numstart = arrTab[1]
    numend = arrTab[2]
    if(colValue>=numstart and colValue <= numend)
    then
        flag = true
    end
    return flag
end

--检查类型 string,int,double,datetime,bool
--local function checkColType(type,colValue)
local function checkColType(type1,colValue)
    flag = false
    if(isempty(type1))
    then
        return flag
    end

    if(type1=="string")
    then
        flag = true
    elseif (type1=="int" or type1=="double")
    then
        if("number"==type(tonumber(colValue)))
        then
            flag = true
        else
            flag = false
        end
    elseif (type1=="bool")
    then
        if("true"==colValue or "false"==colValue)
        then
            flag = true
        else
            flag = false
        end
    elseif (type1=="datetime")
    then
        local year, mon, mday, hour, min, sec = string.match(colValue, "(%d+)-(%d+)-(%d+) (%d+):(%d+):(%d+)")
        --print(year, mon, mday, hour, min, sec)
        if(not isempty(year) and not isempty(mon) and  not isempty(mday) and not isempty(hour) and not isempty(min) and not isempty(sec))
        then
            flag = true
        else
            flag = false
        end
    else
        flag = false
    end
    return flag
end

