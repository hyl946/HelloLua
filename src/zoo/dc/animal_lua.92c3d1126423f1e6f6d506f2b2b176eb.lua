--根据老的字段名 获取新字段名 如果没有就原值返回
--@param oldAttr 老的字段名称
function LuaDpSDK:getNewAttr(oldAttr)
    if(oldAttr==nil)
    then
        return oldAttr
    end
    if(ATTR_OLD_NEW_MAP_SIZE==0) then
        return oldAttr
    end
    if(tableHasKey(ATTR_OLD_NEW_MAP,oldAttr)) then
        return ATTR_OLD_NEW_MAP[oldAttr]
    else
        return oldAttr
    end
end

--根据旧的点号返回新的点号  不包括101
--@param oldAcType  老的点号
function LuaDpSDK:getNewAcType(oldAcType)
    if(oldAcType==nil) then
        return oldAcType
    end
    if(ACTYPE_OLD_NEW_MAP_SIZE==0) then
        return oldAcType
    end
    local key = LuaDpSDK.MAP_KEY_PREFIX..oldAcType
    if(tableHasKey(ACTYPE_OLD_NEW_MAP,key)) then
        return ACTYPE_OLD_NEW_MAP[key]
    else
        return oldAcType
    end
end

--目前为拆分101表使用，根据点号和category 返回新的调号
--如果没有category的mapping  返回useraction_unknown_category
--@param oldAcType 老的点号
--@param category  老的category
function LuaDpSDK:getNewAcTypeSplit(oldAcType,category)
    if(oldAcType==nil) then
        return nil
    end
    if(ACTYPE_OLD_NEW_MAP_SIZE==0)then
        return category
    end
	if(category == nil) then
        return ACTYPE_OLD_NEW_MAP[LuaDpSDK.MAP_KEY_PREFIX..oldAcType.."_*"]
    end
    local key = LuaDpSDK.MAP_KEY_PREFIX..oldAcType.."_"..category
    if(tableHasKey(ACTYPE_OLD_NEW_MAP,key)) then
        return ACTYPE_OLD_NEW_MAP[key]
    else
        return ACTYPE_OLD_NEW_MAP[LuaDpSDK.MAP_KEY_PREFIX..oldAcType.."_*"]
    end
end

--判断一个点号的 属性是否在schema中 方便生成最后的extractMap
--@param newActype
--@param newAttr
function LuaDpSDK:isExistInSchema(newActype,newAttr)
    local result = false
    if(nil == newActype or nil == newAttr) then
        return result
    end

    if(tableHasKey(schemaMap,newActype)) then
        if(newAttr==LuaDpSDK.POINT_TYPE or newAttr==LuaDpSDK.POINT_APPID or newAttr==LuaDpSDK.POINT_GID) then
            result = true
        else
            result = tableHasKey(schemaMap[newActype],newAttr)
        end
    end
    return result
end

