--核心函数新增就校验extractmapsize 不能超过200
function LuaDpSDK:checkRequest(logMap,extractMap)
    local pr = LuaDpSDK:getRequestStr(logMap)
    if(tableHasKey(logMap,LuaDpSDK.EXTRACT_MAP) and nil==extractMap ) then
        pr:SetSuccess(false)
        pr:SetErrorReason(string.format("if log map contain %s, param extractMap must is not null",LuaDpSDK.EXTRACT_MAP))
        return pr
    end
    if(pr:GetSuccess())
    then
        --校验extractmap size
        if (extractMap and tablelength(extractMap)>=200) then
            pr:SetSuccess(false)
            pr:SetErrorReason(string.format("kv size in extractMap need less than 200"))
        end

    end
    return pr;
end


--核心函数
function LuaDpSDK:getRequestStr(logMap)
    local result = self:PackageResponse(false,nil,nil)
    result:SetSuccess(false)
    --print(tablelengthlogMap)
    if(nil==logMap or tablelength(logMap) ==0) then
        result:SetErrorReason(string.format("log map is null or empty"))
        return result
    end

    --0 判断type、appid、gid是否在日志记录汇总 因为type、appid、gid没有在schema中定义，所以此处硬编码
    if(not tableHasKey(logMap,LuaDpSDK.POINT_TYPE))
    then
        result:SetErrorReason(string.format("point must has col %s",LuaDpSDK.POINT_TYPE))
        return result
    end
    local point = logMap[LuaDpSDK.POINT_TYPE]
    --判断点号是否在schema中
    if(not tableHasKey(schemaMap,point))then
        result:SetErrorReason(string.format("point %s not in schma",point))
        return result
    end
    if(not tableHasKey(logMap,LuaDpSDK.POINT_APPID))
    then
        result:SetErrorReason(string.format("point must has col %s",LuaDpSDK.POINT_APPID))
        return result
    end
    if((not tableHasKey(logMap,LuaDpSDK.POINT_GID)) and point~="user_load" and point~="game_install")
    then
        result:SetErrorReason(string.format("point must has col %s",LuaDpSDK.POINT_GID))
        return result
    end
    --1 判断字段个数是否匹配
    local pointMap = schemaMap[point]
    if((tablelength(pointMap) + 3) < tablelength(logMap)) then
        result:SetErrorReason("log map size more than schema column size")
        return result
    end
    --2 判断必传字段
    for col, colCheckMap in pairs(pointMap) do
        --服务器端必传
        if ("true"==colCheckMap[LuaDpSDK.CHECK_MANDATORY] and  (not tableHasKey(logMap,col))) then
            result:SetErrorReason(string.format("point must has col %s,col %s is mandatory",col,col))
            return result
        end
        --客户端必传
        if ("true"==colCheckMap[LuaDpSDK.CHECK_CLIENT_MANDATORY] and (not tableHasKey(logMap,col))) then
            result:SetErrorReason(string.format("point must has col %s,col %s is client mandatory",col,col))
            return result
        end
    end
    --3 类型和range校验
    for logCol, logColValue in pairs(logMap) do
        local isContinue = false--用来代替没有continue问题
        --0.0 这三个字段不做检查 因为没有在schema中
        if (logCol==LuaDpSDK.POINT_TYPE or logCol==LuaDpSDK.POINT_APPID or logCol==LuaDpSDK.POINT_GID) then
            local illegalCharacter = checkLogValue(logColValue)
            if(illegalCharacter) then
                result:SetErrorReason(string.format("col %s  value is %s include illegal character %s, illegal character set is %s",logCol,logColValue,illegalCharacter,LuaDpSDK.ILLEGAL_CHARACTER))
                return result
            else
                isContinue = true
            end
        end
        if(not isContinue) then
            isContinue = false
            -- 0.1 判断字段是否在schema中
            if (not tableHasKey(pointMap,logCol)) then
                --不包含这个字段 则返回异常
                result:SetErrorReason(string.format("col %s not in point",logCol))
                return result
            end
            --1.1 extractmap字段直接透传 不做类型和range校验
            if (logCol==LuaDpSDK.EXTRACT_MAP) then
                local illegalCharacter = checkLogValueExtractMap(logColValue)
                if(illegalCharacter) then
                    result:SetErrorReason(string.format("col %s  value is %s include illegal character %s, illegal character set is %s",logCol,logColValue,illegalCharacter,LuaDpSDK.ILLEGAL_CHARACTER_EXTRACT_MAP))
                    return result
                else
                    isContinue = true
                end
            else
                --1.0 检查value是否包含特殊字符
                local illegalCharacter = checkLogValue(logColValue)
                if(illegalCharacter) then
                    result:SetErrorReason(string.format("col %s  value is %s include illegal character %s, illegal character set is %s",logCol,logColValue,illegalCharacter,LuaDpSDK.ILLEGAL_CHARACTER))
                    return result
                end
            end
            if(not isContinue) then
                --2.0 获取字段 检验类型,range
                local colCheckMap = pointMap[logCol]
                if (tableHasKey(colCheckMap,LuaDpSDK.CHECK_TYPE)) then
                    --类型检查
                    local schemaColType = colCheckMap[LuaDpSDK.CHECK_TYPE]
                    if (not checkColType(schemaColType,logColValue)) then
                        result:SetErrorReason(string.format("point %s's col %s type must is %s",point,logCol,schemaColType))
                        return result
                    end
                end

                if (tableHasKey(colCheckMap,LuaDpSDK.CHECK_RANGE)) then
                    --range检查
                    local range = colCheckMap[LuaDpSDK.CHECK_RANGE]
                    local checkType = colCheckMap[LuaDpSDK.CHECK_TYPE]
                    if (not checkColRange(range,logColValue,checkType)) then
                        result:SetErrorReason(string.format("point %s's col %s range is ,but log's value is %s",point,logCol,range,logColValue))
                        return result
                    end
                end
            end
        end
    end
    logMap[LuaDpSDK.SCHEMA_CLENT_VERSION_KEY]=LuaDpSDK.SCHEMA_CLENT_VERSION_VALUE
    result:SetSuccess(true)
    return result
end
